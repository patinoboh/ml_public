#!/usr/bin/env python3
import argparse
import lzma
import os
import pickle
import sys
from typing import Optional
import urllib.request

import numpy as np
import numpy.typing as npt

import sklearn
import sklearn.naive_bayes
import sklearn.feature_extraction
import sklearn.pipeline
import sklearn.model_selection

parser = argparse.ArgumentParser()
# These arguments will be set appropriately by ReCodEx, even if you change them.
parser.add_argument("--predict", default=None, type=str, help="Path to the dataset to predict")
parser.add_argument("--recodex", default=False, action="store_true", help="Running in ReCodEx")
parser.add_argument("--seed", default=42, type=int, help="Random seed")
# For these and any other arguments you add, ReCodEx will keep your default value.
parser.add_argument("--model_path", default="isnt_it_ironic.model", type=str, help="Model path")


class Dataset:
    def __init__(self,
                 name="isnt_it_ironic.train.txt",
                 url="https://ufal.mff.cuni.cz/~courses/npfl129/2324/datasets/"):
        if not os.path.exists(name):
            print("Downloading dataset {}...".format(name), file=sys.stderr)
            licence_name = name.replace(".txt", ".LICENSE")
            urllib.request.urlretrieve(url + licence_name, filename=licence_name)
            urllib.request.urlretrieve(url + name, filename="{}.tmp".format(name))
            os.rename("{}.tmp".format(name), name)

        # Load the dataset and split it into `data` and `target`.
        self.data = []
        self.target = []

        with open(name, "r", encoding="utf-8-sig") as dataset_file:
            for line in dataset_file:
                label, text = line.rstrip("\n").split("\t")
                self.data.append(text)
                self.target.append(int(label))
        self.target = np.array(self.target, np.int32)

def get_model():
    #este alpha 
    naive_bayes = sklearn.naive_bayes.MultinomialNB()
    tfidf = sklearn.feature_extraction.text.TfidfVectorizer(analyzer = "char", lowercase = False, ngram_range = (1,4))
    
    model = sklearn.pipeline.Pipeline([
        ("Features", tfidf),
        ("MultinomialNB", naive_bayes),])
    
    # create gridsearchcv
    grid = {"MultinomialNB__alpha": np.linspace(0.001, 0.7, 7), "Features__ngram_range": [(1,4), (1,5)], 
            "Features__analyzer": ["char", "word", "char_wb"], "Features__lowercase": [True, False]}
    
    model = sklearn.model_selection.GridSearchCV(estimator=model, 
                                                 cv = sklearn.model_selection.StratifiedKFold(5), 
                                                 param_grid=grid, 
                                                 n_jobs=2, 
                                                 refit=True)
    
    # where to put max verbose while training model?
    model.verbose = 10
    return model

def main(args: argparse.Namespace) -> Optional[npt.ArrayLike]:
    if args.predict is None:
        # We are training a model.
        np.random.seed(args.seed)
        train = Dataset()

        # TODO: Train a model on the given dataset and store it in `model`.
        model = get_model()
        
        model.fit(train.data, train.target)
        print(model.best_params_)

        # Serialize the model.
        with lzma.open(args.model_path, "wb") as model_file:
            pickle.dump(model, model_file)

    else:
        # Use the model and return test set predictions.
        test = Dataset(args.predict)

        with lzma.open(args.model_path, "rb") as model_file:
            model = pickle.load(model_file)

        # TODO: Generate `predictions` with the test set predictions, either
        # as a Python list or a NumPy array.
        predictions = model.predict(test.data)

        return predictions


if __name__ == "__main__":
    args = parser.parse_args([] if "__file__" not in globals() else None)
    main(args)

